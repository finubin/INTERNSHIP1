""" What are comments in Python? 
 ○ How do you write a single-line comment in Python?
 ○ How do you write a multi-line comment in Python? """

print("""Comments in Python are used to explain code and make it more readable.
They are ignored by the Python interpreter.""")

print("To write a single-line comment in Python, you use the # symbol followed by your comment. For example:")
print("# This is a single-line comment")
print("To write a multi-line comment in Python, you use triple quotes (\"\"\" or ''') to enclose your comment. For example:")
print("\"\"\" This is a multi-line comment \"\"\"")
